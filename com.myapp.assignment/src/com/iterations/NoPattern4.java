package com.iterations;

public class NoPattern4 {

	public static void main(String args[]) { 
		//int num=1;
		for(int i=1;i<=5;i++) {
			for(int j=i;j<=5;j++) { 
			 {
				System.out.print(j +" ");
				
				}
				
			}
			System.out.println("");
		}
		for(int i=4;i>=1;i--) {
			for(int j=i;j<=5;j++) {
				System.out.print(j +" ");
			}
			System.out.println("");
		}
	}


	}
